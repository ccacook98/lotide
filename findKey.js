const assertEqual = function(actual, expected) {
  if (actual === expected) {
    console.log(`[O] Assertion Passed: ${actual} === ${expected}`);
  } else {
    console.log(`[X] Assertion Failed: ${actual} !== ${expected}`);
  }
};

const findKey = function(obj, func) {
  let output;
  for (const key in obj) {
    if (func(obj[key])) {
      output = key;
      break;
    }
  }
  return output;
}

